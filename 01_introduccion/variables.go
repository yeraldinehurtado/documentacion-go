package main

import "fmt"

func main4() {

	var a string = "initial"
	fmt.Println(a) // imprime -> initial

	f := "short" // forma corta de declarar variables
	fmt.Println(f) // imprime -> short

	var b, c int = 1, 2
	fmt.Println(b, c) // imprime -> 1 2

	var d = true
	fmt.Println(d) // imprime -> true

	var e int
	fmt.Println(e) // imprime -> 0

}
